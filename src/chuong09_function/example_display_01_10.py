def show_1_to_10():
    for i in range(11):
        print(i)


show_1_to_10()


def show():
    for i in range(1, 11):
        # end = " " có nghĩa là sử dụng hàm print và không xuống dòng
        print(i, end=" ")  # Cú pháp này sử dụng để in ra đoạn mã như thế nào ?
    print()


# gọi tới hàm show
show()
show()
